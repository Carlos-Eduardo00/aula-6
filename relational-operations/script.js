//Menor que <
const menor = 10 < 20;
console.log("10 < 20?", menor);

//Maior que >
const maior = 10 > 20;
console.log("10 > 20?", maior);

//Igual a ==
const igual = 5 == 5;
console.log("5 == 5?", igual);

//Diferente de !=
const diferente = "a" != "b";
console.log("a != b", diferente);

//Menor ou gual a  <=
const menorOuigual = 10 <= 5;
console.log("10 <= 5", menorOuigual);

//Maior ou gual a  >=
const maiorOuigual = 10 >= 5;
console.log("10 >= 5", maiorOuigual);